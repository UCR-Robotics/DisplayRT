class PublisherNode
{
public:
    PublisherNode()
    {
        // Initialize the ROS publisher
        pub_ = nh_.advertise<std_msgs::String>("chatter", 1000);
    }

    void run()
    {
        ros::Rate loop_rate(10); // 10 Hz
        int count = 0;

        while (ros::ok())
        {
            std_msgs::String msg;
            std::stringstream ss;
            ss << "Hello world " << count++;
            msg.data = ss.str();

            ROS_INFO("[Publisher] Publishing: %s", msg.data.c_str());
            pub_.publish(msg);

            ros::spinOnce();
            loop_rate.sleep();
        }
    }

private:
    ros::NodeHandle nh_;
    ros::Publisher pub_;
};

class SubscriberNode
{
public:
    SubscriberNode()
    {
        // Initialize the ROS subscriber
        sub_ = nh_.subscribe("chatter", 1000, &SubscriberNode::callback, this);
    }

    void run()
    {
        ros::spin(); // Keep processing callbacks
    }

private:
    void callback(const std_msgs::String::ConstPtr &msg)
    {
        ROS_INFO("[Subscriber] Received: %s", msg->data.c_str());
    }

    ros::NodeHandle nh_;
    ros::Subscriber sub_;
};

